package com.example.sae_s2_01;

public class StringInt {
    private String str;
    private int nbr;
    public StringInt (String str, int nbr){
        this.str = str;
        this.nbr = nbr;
    }

    public String getStr() {
        return str;
    }

    public int getNbr() {
        return nbr;
    }
}
